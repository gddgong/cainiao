package com.xcxgf.cainiao.POJO;

public class ExcelData {
    private String companyName;
    private String contact;
    private String contactNumber;
    private String dormitoryId;
    private String startDate;
    private String endDate;
    private String leasePeriod;
    private String remark;
    private String dromNum;

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }

    public String getContactNumber() {
        return contactNumber;
    }

    public void setContactNumber(String contactNumber) {
        this.contactNumber = contactNumber;
    }

    public String getDormitoryId() {
        return dormitoryId;
    }

    public void setDormitoryId(String dormitoryId) {
        this.dormitoryId = dormitoryId;
    }

    public String getStartDate() {
        return startDate;
    }

    public void setStartDate(String startDate) {
        this.startDate = startDate;
    }

    public String getEndDate() {
        return endDate;
    }

    public void setEndDate(String endDate) {
        this.endDate = endDate;
    }

    public String getLeasePeriod() {
        return leasePeriod;
    }

    public void setLeasePeriod(String leasePeriod) {
        this.leasePeriod = leasePeriod;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public String getDromNum() {
        return dromNum;
    }

    public void setDromNum(String dromNum) {
        this.dromNum = dromNum;
    }
}
