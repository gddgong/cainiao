package com.xcxgf.cainiao.POJO;

public class Setting {
    private int id;
    private String rentUnitPrice;
    private String manageExpense;
    private int state;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getRentUnitPrice() {
        return rentUnitPrice;
    }

    public void setRentUnitPrice(String rentUnitPrice) {
        this.rentUnitPrice = rentUnitPrice;
    }

    public String getManageExpense() {
        return manageExpense;
    }

    public void setManageExpense(String manageExpense) {
        this.manageExpense = manageExpense;
    }

    public int getState() {
        return state;
    }

    public void setState(int state) {
        this.state = state;
    }
}
